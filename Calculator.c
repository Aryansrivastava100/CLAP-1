#include <stdio.h>
int main()
{
    int a,b,multiply,divide,sum,subtract;
    printf("Enter the value of A: ");
    scanf("%d",&b);
    printf("Enter the value of B: ");
    scanf("%d",&a);
    sum=a+b;
    subtract=b-a;
    multiply=a*b;
    divide=a/b;
    printf("sum = %d\n",sum);
    printf("subtract = %d\n",subtract);
    printf("multiply = %d\n",multiply);
    printf("divide = %d\n",divide);
    return 0;
} 
